export type Language = 'en' | 'fr';
export type TranslationKey = typeof translations.en;

// Safe translation function to prevent crashes
export const getTranslation = (key: string, lang: Language = 'fr'): string => {
  try {
    const keys = key.split('.');
    let value: any = translations[lang] || translations.en;
    
    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        // Fallback to French if key not found
        value = translations.fr;
        for (const k of keys) {
          if (value && typeof value === 'object' && k in value) {
            value = value[k];
          } else {
            // Try English as last resort
            value = translations.en;
            for (const k of keys) {
              if (value && typeof value === 'object' && k in value) {
                value = value[k];
              } else {
                return key; // Return key itself if not found
              }
            }
          }
        }
      }
    }
    
    return typeof value === 'string' ? value : key;
  } catch (error) {
    console.error('[TRANSLATIONS] Error accessing translation key:', key, error);
    return key;
  }
};